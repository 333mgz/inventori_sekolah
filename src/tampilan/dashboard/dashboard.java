    private void tampilGrafik(){

    DefaultCategoryDataset dataset =
    new DefaultCategoryDataset();

    dataset.setValue(20,
    "Barang","Januari");

    dataset.setValue(15,
    "Barang","Februari");

    dataset.setValue(30,
    "Barang","Maret");

    JFreeChart chart =
    ChartFactory.createBarChart(
    "Grafik Inventori",
    "Bulan",
    "Jumlah",
    dataset);

    ChartPanel cp =
    new ChartPanel(chart);

    panelGrafik.removeAll();

    panelGrafik.setLayout(
    new java.awt.BorderLayout());

    panelGrafik.add(cp);

    panelGrafik.validate();
}
    
    private void tampilAktivitas(){

    DefaultTableModel model =
    new DefaultTableModel();

    model.addColumn("ID");
    model.addColumn("Tanggal");
    model.addColumn("Aktivitas");

    try{

        Connection conn =
        Koneksi.getKoneksi();

        Statement st =
        conn.createStatement();

        ResultSet rs =
        st.executeQuery(
        "SELECT id_transaksi,tanggal,'Barang Masuk' aktivitas " +
        "FROM barang_masuk LIMIT 10");

        while(rs.next()){

            model.addRow(new Object[]{

                rs.getString("id_transaksi"),
                rs.getString("tanggal"),
                rs.getString("aktivitas")
            });
        }

        tblAktivitas.setModel(model);

    }catch(Exception e){

        JOptionPane.showMessageDialog(
        null,e);
    }
}
    
