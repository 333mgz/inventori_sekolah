     private void tampilData() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Nama");
    model.addColumn("Kategori");
    model.addColumn("Stok");
    model.addColumn("Kondisi");
    model.addColumn("Lokasi");

    try {
        String sql = "SELECT * FROM barang";
        ResultSet rs = Koneksi.getKoneksi().createStatement().executeQuery(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("id_barang"),
                rs.getString("nama_barang"),
                rs.getString("id_kategori"),
                rs.getString("stok"),
                rs.getString("kondisi"),
                rs.getString("lokasi")
            });
        }

        tableBarang.setModel(model);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
}

    private void loadKategori() {
    try {
        cbKategori.removeAllItems();

        String sql = "SELECT * FROM kategori";
        ResultSet rs = Koneksi.getKoneksi().createStatement().executeQuery(sql);

        while (rs.next()) {
            cbKategori.addItem(rs.getString("nama_kategori"));
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
}

    private void autoId() {
    try {
        String sql = "SELECT MAX(id_barang) FROM barang";
        ResultSet rs = Koneksi.getKoneksi().createStatement().executeQuery(sql);

        if (rs.next() && rs.getString(1) != null) {
            String id = rs.getString(1); // contoh: BRG005
            int angka = Integer.parseInt(id.substring(3)); // ambil 005 → 5
            angka++;

            txtId.setText(String.format("BRG%03d", angka));
        } else {
            txtId.setText("BRG001");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
        txtId.setText("BRG001");
    }
}
